def delivery_status(request):
	user = request.user
	delivery_status = DeliveryStatus.objects.filter(user=user).last()  # Assuming you want to retrieve the latest delivery status
	#messages = DeliveryStatus.objects.values_list('message', flat=True)  # Retrieve all messages from the database
	last_order =  Order.objects.filter(user=user).last()
	order_items = OrderItem.objects.filter(order=last_order)

	#serialize order items into a string
	cart_info=""
	for item in order_items:
		cart_info += f"{item.product.name}(x{item.quantity})\n"
	#Save cart_info to DeliveryStatus model
		delivery_status.cart_info = cart_info
		delivery_status.save()

		total_price = 0
		for item in order_items:
			total_price += item.product.price * item.quantity
			delivery_status.total_price = total_price
			delivery_status.save()


	return render(request, 'delivery_status.html', {
		'delivery_status': delivery_status,
		'order_items': order_items,
		'total_price': total_price,
      #  'messages': messages,
    })
	#messages.get_messages(request)



def search(request):
	# Determine if they filled out the form
	if request.method == "POST":
		searched = request.POST['searched']
		# Query The Products DB Model
		searched = Product.objects.filter(Q(name__icontains=searched) | Q(description__icontains=searched))
		# Test for null
		if not searched:
			messages.success(request, "That Product Does Not Exist...Please try Again.")
			return render(request, "search.html", {})
		else:
			return render(request, "search.html", {'searched':searched})
	else:
		return render(request, "search.html", {})	


